#include <stdio.h>
#include <stdlib.h>

int main()
{
    char a;
    printf ("write char value:");
    scanf("%s",&a);
    printf("character %c\n",a);
    printf("decimal %d\n",a);
    printf("octal %o\n",a);
    printf("hexadecimal %x",a);
    return 0;
}
